package model;

public class LoginInfo {

	private String uid;
	private String pw;
	
	public LoginInfo(String uid, String pw) {

		this.uid = uid;
		this.pw = pw;
	}

	public String getUid() {
		return uid;
	}

	
	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}
	
	
	
	
	
	
}
